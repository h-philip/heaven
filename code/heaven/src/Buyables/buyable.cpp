#include "buyable.h"

#include "../Characters/character.h"
#include "../Characters/player.h"

namespace Buyables {

///////////////////////////////////////////////////////////////////////////////////////////////////
/* Buyable() */
///////////////////////////////////////////////////////////////////////////////////////////////////
Buyable::Buyable(const sf::Texture& texture) {
  this->sprite.setTexture(texture);
  this->sprite.setColor(sf::Color(100, 100, 200, 200));
}

///////////////////////////////////////////////////////////////////////////////////////////////////
/* draw() */
///////////////////////////////////////////////////////////////////////////////////////////////////
void Buyable::draw(sf::RenderWindow* window) { window->draw(this->sprite); }

///////////////////////////////////////////////////////////////////////////////////////////////////
/* draw() */
///////////////////////////////////////////////////////////////////////////////////////////////////
void Buyable::draw(sf::RenderWindow* window, const sf::Vector2f& position) {
  this->sprite.setPosition(position);
  draw(window);
}

///////////////////////////////////////////////////////////////////////////////////////////////////
/* assignTo() */
///////////////////////////////////////////////////////////////////////////////////////////////////
void Buyable::assignTo(Characters::Character* character) {
  if (!character) {
    std::cerr << "Error while assigning character to buyable: "
              << "character is nullptr!" << std::endl;
    exit(1);
  }
  this->character = character;
}
}  // namespace Buyables