#include "network_button.h"

namespace Grounds {

///////////////////////////////////////////////////////////////////////////////////////////////////
/* Static Variables */
///////////////////////////////////////////////////////////////////////////////////////////////////

const std::string NetworkButton::TEXTURE_FILE =
    "Ressources/Images/Grounds/Buttons/network_button.png";

sf::Texture NetworkButton::texture;

bool NetworkButton::is_pressed = false;

///////////////////////////////////////////////////////////////////////////////////////////////////
/* getType() */
///////////////////////////////////////////////////////////////////////////////////////////////////
ButtonType NetworkButton::getType() const { return ButtonType::NetworkButton; }

///////////////////////////////////////////////////////////////////////////////////////////////////
/* name() */
///////////////////////////////////////////////////////////////////////////////////////////////////
std::string NetworkButton::name() const {
  return ButtonHelper::typeToName(getType());
}

///////////////////////////////////////////////////////////////////////////////////////////////////
/* doAction() */
///////////////////////////////////////////////////////////////////////////////////////////////////
void NetworkButton::doAction(Characters::Player* p, float factor) {
  NetworkButton::is_pressed = true;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
/* isPressed() */
///////////////////////////////////////////////////////////////////////////////////////////////////
bool NetworkButton::isPressed() { return NetworkButton::is_pressed; }

///////////////////////////////////////////////////////////////////////////////////////////////////
/* press() */
///////////////////////////////////////////////////////////////////////////////////////////////////
void NetworkButton::press(bool value) { NetworkButton::is_pressed = value; }
}  // namespace Grounds