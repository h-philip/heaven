#include "vert_movement.h"

namespace Characters {

///////////////////////////////////////////////////////////////////////////////////////////////////
/* applyForce() */
///////////////////////////////////////////////////////////////////////////////////////////////////
void VertMovement::applyForce(float force) {
  applyForce(force, 1);
  this->current_speed += force;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
/* applyForce() */
///////////////////////////////////////////////////////////////////////////////////////////////////
void VertMovement::applyForce(float force, float factor) {
  this->current_speed += force * factor;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
/* applyGravity() */
///////////////////////////////////////////////////////////////////////////////////////////////////
void VertMovement::applyGravity(float factor) {
  this->current_speed += this->GRAVITY * factor;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
/* move() */
///////////////////////////////////////////////////////////////////////////////////////////////////
float VertMovement::move(float factor) const {
  // If current_speed > max_speed then use max_speed else use current_speed
  return ((this->current_speed > this->max_speed ? this->max_speed
                                                 : this->current_speed) *
          factor);
}
}  // namespace Characters