#include "heart.h"

namespace Collectables {

///////////////////////////////////////////////////////////////////////////////////////////////////
/* Static Variables */
///////////////////////////////////////////////////////////////////////////////////////////////////

const std::string Heart::TEXTURE_FILE =
    "Ressources/Images/Characters/Player/hearts.png";
const std::string Heart::SOUND_FILE = "Ressources/Sounds/heal.wav";
const sf::Vector2i Heart::SIZE = sf::Vector2i(36, 30);

sf::Texture Heart::texture;
sf::SoundBuffer Heart::sound_buffer;

///////////////////////////////////////////////////////////////////////////////////////////////////
/* collect() */
///////////////////////////////////////////////////////////////////////////////////////////////////
void Heart::collect() {
  Collectable::collect();
  this->sound.play();
}

///////////////////////////////////////////////////////////////////////////////////////////////////
/* canBeDelted() */
///////////////////////////////////////////////////////////////////////////////////////////////////
bool Heart::canBeDeleted() {
  return (this->sound.getStatus() != sf::Sound::Status::Playing);
}
}  // namespace Collectables